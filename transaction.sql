CREATE TABLE TRANSACTION (
    TxnID VARCHAR(5) PRIMARY KEY,
    PaymentType VARCHAR(20),
    Amount INT,
    TxnDate DATE,
    ProductID VARCHAR(5),
    BuyerID VARCHAR(5),
    CONSTRAINT fk_txn_product
        FOREIGN KEY (ProductID)
        REFERENCES PRODUCT(ProductID),
    CONSTRAINT fk_txn_buyer
        FOREIGN KEY (BuyerID)
        REFERENCES BUYER(BuyerID)
);

