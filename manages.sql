CREATE TABLE MANAGES (
    SellerID VARCHAR(5),
    EmpID VARCHAR(5),
    ManageDate DATE,
    CONSTRAINT pk_manages
        PRIMARY KEY (SellerID, EmpID),
    CONSTRAINT fk_manage_seller
        FOREIGN KEY (SellerID)
        REFERENCES SELLER(SellerID),
    CONSTRAINT fk_manage_emp
        FOREIGN KEY (EmpID)
        REFERENCES EMPLOYEE(EmpID)
);


