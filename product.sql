    ProductID VARCHAR(5) PRIMARY KEY,
    ProductName VARCHAR(50),
    Price INT,
    Category VARCHAR(30),
    SellerID VARCHAR(5),
    CONSTRAINT fk_product_seller
        FOREIGN KEY (SellerID)
        REFERENCES SELLER(SellerID)
);


